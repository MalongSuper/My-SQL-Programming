-- Works_On Table
CREATE TABLE Works_On (
  ESSN VARCHAR(9),
  PNo INT,
  Hours NUMERIC(3, 1),
  CONSTRAINT chk_Hours CHECK(Hours <= 40.0 and Hours  > 0),
  FOREIGN KEY (ESSN) REFERENCES Employee(SSN),
  FOREIGN KEY (PNo) REFERENCES Project(PNumber)
);
GO

